number = input("Enter a number to see the corrosponding day:")

if number == "1":
    print("Sunday")
if number == "2":
    print("Monday")
if number == "3":
    print("Tuesday")
if number == "4":
    print("Wednesday")
if number == "5":
    print("Thursday")
if number == "6":
    print("Friday")
if number == "7":
    print("Saturday")
else:
    print("Enter a number between 1 to 7") 